#include <stdio.h>
#include <math.h>

int main(){
    
    //Redondeamos con ceil hacia arriba
    printf("Redondeo, %f\n", ceil(5.4));

    //Devolvemos el absoluto
    printf("Absoluto, %f\n", fabs(-4));

    //Rendondeamos hacia abajo
    printf("Floor: %f\n", floor(5.7));

    //Raiz cuadrada
    printf("Raiz: %f\n", sqrt(100));

    //Calcula el resto de la división
    // printf("Modulo: %i", fmod(7, 2));

    //elevación
    printf("Elevacion: %f\n", pow(2, 3));

}
